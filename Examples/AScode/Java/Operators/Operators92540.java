class Main {
    public static void main(String[] args) {
      
    //   declare variables
      int a = 7, b = 9;
      int result1, result2;
  
    //   original value
      System.out.println("Value of a: " + a);
    //   Value of a: 7
  
    //   increment operator
      result1 = ++a;
      System.out.println("After increment: " + result1);
    //   After increment: 8
  
      System.out.println("Value of b: " + b);
    //   Value of b: 9
  
    //   decrement operator
      result2 = --b;
      System.out.println("After decrement: " + result2);
    //   After decrement: 8
    }
  }
