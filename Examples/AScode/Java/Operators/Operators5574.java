class Main {
    public static void main(String[] args) {
      
      int a = 2;
      int variable;
  
    //   assignment
      variable = a;
      System.out.println("var using =: " + variable);
    //   var using =: 2
  
    //   =+ operator 
      variable += a;
      System.out.println("var using +=: " + variable);
    //   var using +=: 4
  
    //   =* operator
      variable *= a;
      System.out.println("var using *=: " + variable);
    //   var using *=: 8
    }
  }
