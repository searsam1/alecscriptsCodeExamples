class Main {
    public static void main(String[] args) {
      
      // declare variables
      int a = 2, b = 3;
  
      // addition operator
      System.out.println("a + b = " + (a + b));
      // a + b = 5
  
      // subtraction operator
      System.out.println("a - b = " + (a - b));
      // a - b = -1
  
      // multiplication operator
      System.out.println("a * b = " + (a * b));
      // a * b = 6
  
      // division operator
      System.out.println("a / b = " + (a / b));
      // a / b = 0
  
      // modulo operator
      System.out.println("b % a = " + (b % a));
      // b % a = 1
    }
  }
