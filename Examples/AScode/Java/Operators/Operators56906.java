class Main {
    public static void main(String[] args) {
      
      // create variables
      int a = 55, b = 5;
  
      // value of a and b
      System.out.println("a is " + a + " and b is " + b);
      // a is 55 and b is 5
  
      // == operator
      System.out.println(a == b);
      // false
  
      // != operator
      System.out.println(a != b);  
      // true
  
      // > operator
      System.out.println(a > b);  
      // true
  
      // < operator
      System.out.println(a < b);  
      // false
  
      // >= operator
      System.out.println(a >= b);  
      // true
  
      // <= operator
      System.out.println(a <= b);  
      // false
    }
  }
  
