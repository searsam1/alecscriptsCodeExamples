class Main {
    public static void main(String[] args) {    
        int num = 10;
        // Define new switch case
        switch(num) {
            // if num == 10: 
            case 10:
                System.out.println("Num is 10");
                break;
            // if num == 20: 
            case 20:
                System.out.println("Num is 20");
                break;
            // else:
            default:
                System.out.println("Num is not 10 or 20");
        }
        // Output - "Num is 10"
    }
}
